export function getLocale(locale) {
  return translations[locale] ? locale : DEFAULT_LOCALE;
}

function lookupTranslation(locale, key) {
  return key
    .split(".")
    .reduce((current, part) => current?.[part], translations[locale]);
}

export function createTranslator(locale) {
  const safeLocale = getLocale(locale);

  return function t(key, params = {}) {
    const value =
      lookupTranslation(safeLocale, key) ??
      (safeLocale !== "en" ? lookupTranslation("en", key) : undefined) ??
      (safeLocale !== DEFAULT_LOCALE ? lookupTranslation(DEFAULT_LOCALE, key) : undefined);

    if (typeof value !== "string") {
      return key;
    }

    return value.replace(/\{(\w+)\}/g, (_, token) =>
      String(params[token] ?? ""),
    );
  };
}
